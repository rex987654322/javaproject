package com.ums.ums;

public class employee {
    private int id;
    private String name;
    private int salary;
    //construct
    public employee(int id, String name, int salary) {
        this.id = id;
        this.name = name;
        this.salary = salary;
    }
    //getter
    public int getID() { return id;}
    public String getName() { return name;}
    public int getSalary() { return salary;}




}
